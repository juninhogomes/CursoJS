var vel = 50
console.log(`A Velocidade do seu carro é ${vel} Km/h`)

// Se a variável "vel" for maior que 60, ele vai executar a operação do if.
// Se a variável "vel" não for maior que 60, ele vai executar a operação do else.
if (vel > 60) {
    console.log(`Você está acima do limite de velocidade`)
} else {
    console.log(`Utilize sempre cinto de segurança`)
}

